
# ============================================================================= #
#                                                                               #
# input: [1, 2, 9, 9]                                                           #
#                                                                               #
# ----------------------------------------------------------------------------  #
# digits         | [1, 2, 9, 10] | [1, 2, 10, 0] | [1, 3, 0, 0] | [1, 3, 0, 0]  #
# place          |      3        |       2       |       1      |       0       #                                                
# digits[place]  |      10       |       10      |       3      |       1       # 
# ============================================================================= #                                                   
                                                                                                                                           

def plus_one(digits):
    digits[-1] += 1
    for place in range(len(digits)-1, -1, -1):
        if digits[place] is 10:
            digits[place] = 0
            if place > 0:
                digits[place-1] += 1
            else:
                digits.insert(0, 1)
    return digits